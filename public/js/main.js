(() => {
  // <stdin>
  console.log("This site was generated by Hugo.");
  var navbar = null;
  document.addEventListener("DOMContentLoaded", function() {
    navbar = document.querySelector(".navbar.fixed-top");
    adjustBodyPadding();
    adjustCarouselHeight();
  });
  function adjustBodyPadding() {
    if (navbar) {
      document.body.style.paddingTop = navbar.offsetHeight + "px";
    }
  }
  function adjustCarouselHeight() {
    if (navbar) {
      var newHeight = window.innerHeight - navbar.offsetHeight;
      document.querySelectorAll(".carousel-item").forEach(function(item) {
        item.style.height = newHeight + "px";
      });
    }
  }
  window.addEventListener("resize", function() {
    adjustBodyPadding();
    adjustCarouselHeight();
  });
  document.addEventListener("DOMContentLoaded", (event) => {
    gsap.registerPlugin(ScrollTrigger);
    document.querySelectorAll("h1, h2, h3, h4, h5, img").forEach((el) => {
      gsap.from(el, {
        opacity: 0,
        y: 40,
        duration: 1,
        ease: "power2.out",
        scrollTrigger: {
          trigger: el,
          // ðŸ‘ˆ trigger each element individually
          start: "top 90%",
          // start animating when it enters viewport
          toggleActions: "play none none reverse"
        }
      });
    });
  });
})();
